package robot.model;

/**
 * 输出值
 */
public class Values {
    /**
     * 内容地址
     */
    private String url;

    /**
     * 内容文本
     */
    private String text;

    /**
     * 内容地址
     */
    public String getUrl() {
        return url;
    }

    /**
     * 内容地址
     */
    public void setUrl(String url) {
        this.url = url;
    }

    /**
     * 内容文本
     */
    public String getText() {
        return text;
    }

    /**
     * 内容文本
     */
    public void setText(String text) {
        this.text = text;
    }
}
